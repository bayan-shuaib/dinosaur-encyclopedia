export const dinosaurs: Dinosaur[] = [...baseDinosaurs, ...additionalDinosaurs];

export function getDinosaurById(id: string): Dinosaur | undefined {
  return dinosaurs.find(d => d.id === id);
}

export function getDinosaursByGroup(group: string): Dinosaur[] {
  return dinosaurs.filter(d => d.group === group);
}

export function getDinosaursByPeriod(period: string): Dinosaur[] {
  return dinosaurs.filter(d => d.period === period);
}

export function getDinosaursByDiet(diet: string): Dinosaur[] {
  return dinosaurs.filter(d => d.diet === diet);
}

export function searchDinosaurs(query: string): Dinosaur[] {
  const q = query.toLowerCase();
  return dinosaurs.filter(d =>
    d.name.toLowerCase().includes(q) ||
    d.scientificName.toLowerCase().includes(q) ||
    d.group.toLowerCase().includes(q)
  );
}
